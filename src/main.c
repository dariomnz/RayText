#include "Common.h"
#include "Editor.h"
#include "DArray.h"

// Global variables
extern Editor editor;

//------------------------------------------------------------------------------------
// Program main entry point
//------------------------------------------------------------------------------------
int main()
{
    // Initialization
    //--------------------------------------------------------------------------------------
    Editor_Init();

    // SetTargetFPS(60);

    // Main game loop
    while (!WindowShouldClose() && !editor.the_end) // Detect window close button or ESC key
    {
        //----------------------------------------------------------------------------------
        // Logic
        //----------------------------------------------------------------------------------
        Editor_Logic();
        //----------------------------------------------------------------------------------

        //----------------------------------------------------------------------------------
        // Draw
        //----------------------------------------------------------------------------------
        BeginDrawing();

        Editor_Draw();

        EndDrawing();
        //----------------------------------------------------------------------------------
    }

    // De-Initialization
    //--------------------------------------------------------------------------------------
    Editor_Close();
    //--------------------------------------------------------------------------------------
    return 0;
}