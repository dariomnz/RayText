#include "Common.h"
#include "Editor.h"
#include "DArray.h"

//------------------------------------------------------------------------------------
// Program main entry point
//------------------------------------------------------------------------------------
int main(void)
{
    // Initialization
    //--------------------------------------------------------------------------------------
    SetTargetFPS(60);
    SetConfigFlags(FLAG_WINDOW_RESIZABLE);
    
    Editor editor = {0};
    Editor_Init(&editor);


    // Main game loop
    while (!WindowShouldClose() && !editor.the_end) // Detect window close button or ESC key
    {
        //----------------------------------------------------------------------------------
        // Logic
        //----------------------------------------------------------------------------------
        Editor_Logic(&editor);
        //----------------------------------------------------------------------------------

        //----------------------------------------------------------------------------------
        // Draw
        //----------------------------------------------------------------------------------
        BeginDrawing();

        Editor_Draw(&editor);

        EndDrawing();
        //----------------------------------------------------------------------------------
    }

    // De-Initialization
    //--------------------------------------------------------------------------------------
    Editor_Close(&editor);
    //--------------------------------------------------------------------------------------
    return 0;
}